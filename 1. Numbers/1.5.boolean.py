
# True se comporta como 1
verdadero = int(True)
print(verdadero) # 1

# False se comporta como 0
falso = int(False)
print(falso) # 0

# Podemos convertir un número a booleano
print(bool(1)) # True

# Cualquier número diferente de 0 es True
print(bool(-42)) # True

# Quick peek at the operators (and, or, not)
print(True and True) # True
print(True or False) # True
print(not True) # False


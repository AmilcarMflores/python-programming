s = "The trouble is you think you have time."
print(s[0]) # T
print(s[1]) # h

# Último caracter
print(s[-1]) # . 

print(s[:4]) # The
print(s[4:]) # trouble is you think you have time.

print(s[2:14]) # e trouble is

# 2 -> inicio, 14 -> fin, 3 -> salto
print(s[2:14:3]) # 'erb '

# Forma rápida de copiar una cadena
print(s[:]) # The trouble is you think you have time.

# Invertir una cadena
print(s[::-1]) # .emit evah uoy kniht uoy si elburt ehT